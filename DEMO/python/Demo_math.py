import math

print(math.pi)  # π
print(math.e)  # e=2.718

print(math.fabs(-10))  # 绝对值

print(math.pow(2, 10))  # 2^10
print(math.sqrt(16))  # 根号16
print(math.log(math.e ** 2))  # ln x
print(math.log10(10 ** 2))  # log10x

print(math.cos(math.pi))  # cos
print(math.sin(math.pi))  # sin
print(math.tan(math.pi))  # tan

print(math.factorial(5))  # 阶乘，5！

print(math.ceil(3.14))  # 向上取整
print(math.floor(3.14))  # 向下取整
print(math.fmod(20, 3))  # 取余
